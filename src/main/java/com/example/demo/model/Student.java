package com.example.demo.model;

public class Student {
    private Integer id;
    private String name;
    private String no;

    public Integer getId() {
        return id;
    }

    public String getName(){
        return name;
    }

    public String getNo(){
        return no;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setName(String name){
        this.name=name;
    }
    public void setNo(String no){
        this.no=no;
    }
}
